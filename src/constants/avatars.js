// 아바타 유효성 검사
export const isValidAvatar = (emoji) => {
  return ALL_AVATARS.includes(emoji);
};

// 카테고리별 이모지 가져오기
export const getAvatarsByCategory = (categoryId) => {
  const category = Object.values(AVATAR_CATEGORIES).find(
    cat => cat.id === categoryId
  );
  return category ? category.emojis : [];
};
